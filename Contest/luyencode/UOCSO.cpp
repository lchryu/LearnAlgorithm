#include <bits/stdc++.h>
using namespace std;
int cntDiv(int n) {
    int count = 0;
    for (int i = 1; i * i <= n; i++) {
        if (n % i == 0) {
            count++;
            if (i != n / i) count++;
        }
    }
    return count;
}
int main() {
    int n; cin >> n;
    vector<int>a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
        cout << cntDiv(a[i]) << endl;
    }
}
